/* Decodificação CP850 (DOS Latin-1) — usada pelo comando ^FH (Field Hexadecimal)
   das impressoras Zebra/Argox/Elgin para representar acentos em PRNs gerados no Windows.
   Ex.: "Descri\87\C6o" -> "Descrição"  (\87 = ç, \C6 = ã) */

(function (global) {
  // Mapeamento dos bytes 0x80..0xFF do CP850 para Unicode (code points).
  var HIGH = [
    0xC7,0xFC,0xE9,0xE2,0xE4,0xE0,0xE5,0xE7,0xEA,0xEB,0xE8,0xEF,0xEE,0xEC,0xC4,0xC5,
    0xC9,0xE6,0xC6,0xF4,0xF6,0xF2,0xFB,0xF9,0xFF,0xD6,0xDC,0xF8,0xA3,0xD8,0xD7,0x192,
    0xE1,0xED,0xF3,0xFA,0xF1,0xD1,0xAA,0xBA,0xBF,0xAE,0xAC,0xBD,0xBC,0xA1,0xAB,0xBB,
    0x2591,0x2592,0x2593,0x2502,0x2524,0xC1,0xC2,0xC0,0xA9,0x2563,0x2551,0x2557,0x255D,0xA2,0xA5,0x2510,
    0x2514,0x2534,0x252C,0x251C,0x2500,0x253C,0xE3,0xC3,0x255A,0x2554,0x2569,0x2566,0x2560,0x2550,0x256C,0xA4,
    0xF0,0xD0,0xCA,0xCB,0xC8,0x131,0xCD,0xCE,0xCF,0x2518,0x250C,0x2588,0x2584,0xA6,0xCC,0x2580,
    0xD3,0xDF,0xD4,0xD2,0xF5,0xD5,0xB5,0xFE,0xDE,0xDA,0xDB,0xD9,0xFD,0xDD,0xAF,0xB4,
    0xAD,0xB1,0x2017,0xBE,0xB6,0xA7,0xF7,0xB8,0xB0,0xA8,0xB7,0xB9,0xB3,0xB2,0x25A0,0xA0
  ];

  function decodeByte(b) {
    if (b < 0x80) return String.fromCharCode(b);
    return String.fromCharCode(HIGH[b - 0x80]);
  }

  // Substitui sequências <indicador><HH> (hex) pela letra correspondente em CP850.
  // O indicador padrão do ^FH é a barra invertida "\".
  function decodeFieldHex(text, indicator) {
    var ind = indicator || '\\';
    var esc = ind.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); // escapa para regex
    var re = new RegExp(esc + '([0-9A-Fa-f]{2})', 'g');
    return text.replace(re, function (_, hh) {
      return decodeByte(parseInt(hh, 16));
    });
  }

  global.CP850 = { decodeByte: decodeByte, decodeFieldHex: decodeFieldHex };
})(window);
